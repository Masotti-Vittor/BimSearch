	// This method was made with the help of an Artificial Intelligence.
	// The main objective of this method is to 
	// 1. Receive the path where the image is.
	// 2. Show the image in the lbl made inside this JInternalFrame, this window.
	// 3. Modify the size, and center the image.
	public void showImage(String path) {
		try {
			File f = new File(path);
			if (!f.exists()) {
				JOptionPane.showMessageDialog(null, "Arquivo não encontrado: " + path);
				return;
			}

			ImageIcon icon = new ImageIcon(path);
			Image img = icon.getImage();

			int imgWidth = img.getWidth(null);
			int imgHeight = img.getHeight(null);
			int lblWidth = lblImage1.getWidth();
			int lblHeight = lblImage1.getHeight();

			// Se ainda não tiver tamanho definido, usa tamanho padrão
			if (lblWidth <= 0) lblWidth = 300;
			if (lblHeight <= 0) lblHeight = 300;

			// Calcula proporção para manter aspecto
			double widthRatio = (double) lblWidth / imgWidth;
			double heightRatio = (double) lblHeight / imgHeight;
			double scale = Math.min(widthRatio, heightRatio); // menor fator para caber

			int newWidth = (int) (imgWidth * scale);
			int newHeight = (int) (imgHeight * scale);

			Image scaledImg = img.getScaledInstance(newWidth, newHeight, Image.SCALE_SMOOTH);
			icon = new ImageIcon(scaledImg);

			lblImage1.setIcon(icon);
			lblImage1.setHorizontalAlignment(SwingConstants.CENTER);
			lblImage1.setVerticalAlignment(SwingConstants.CENTER);
			lblImage1.repaint();

		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, "Erro ao carregar imagem: " + e.getMessage());
		}
	}



